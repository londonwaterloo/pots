// ЗАДАНИЕ 2 — Последовательность Коллатца
//
// Алгоритм:
// Если n == 1 → последовательность заканчивается.
// Если n чётное → n = n / 2
// Если n нечётное → n = 3n + 1
//
// Нужно вернуть длину последовательности.

/// Определяет длину последовательности Коллатца для числа n.
/// 
/// Пример:
/// n = 11
/// 11 → 34 → 17 → 52 → 26 → 13 → 40 → 20 → 10 → 5 → 16 → 8 → 4 → 2 → 1
/// Всего 15 чисел.
/// 
/// Значит collatz_length(11) = 15
fn collatz_length(mut n: i32) -> u32 {
    // Проверим корректность входных данных.
    // Коллатц определён только для положительных чисел.
    if n <= 0 {
        panic!("n должно быть положительным");
    }

    // length — счётчик длины последовательности.
    // u32 потому что длина не может быть отрицательной.
    let mut length: u32 = 1; 
    // Считаем 1 сразу, потому что само число n входит в последовательность.

    // Пока n не станет равным 1 — продолжаем.
    while n != 1 {

        // Если число чётное
        if n % 2 == 0 {
            n = n / 2;
        } 
        // Если число нечётное
        else {
            n = 3 * n + 1;
        }

        // Каждый шаг увеличивает длину
        length += 1;
    }

    // Возвращаем длину
    length
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_collatz_length() {
        // Проверяем, что для 11 длина = 15
        assert_eq!(collatz_length(11), 15);
    }
}

fn main() {
    println!("Длина: {}", collatz_length(11));
}