// ======================================================
// ЗАДАНИЕ 4 — Геометрические вычисления
//
// Нужно реализовать:
// 1) magnitude(v)  — длина (модуль) вектора
// 2) normalize(v)  — нормализация вектора (сделать длину = 1)
//
// Вектор у нас 3D, но мы сделаем функции универсальными для любого размера,
// потому что Rust легко работает со "срезами" (&[f64]).
//
// Формулы:
// magnitude(v) = sqrt(x^2 + y^2 + z^2 + ...)
// normalize(v): v[i] = v[i] / magnitude(v)
// ======================================================

/// Вычисляет модуль (длину) вектора.
///
/// Принимаем `&[f64]` — это "срез":
/// - можно передать и массив `[f64; 3]` (как в задании),
/// - и вектор `Vec<f64>`,
/// - и вообще любую последовательность f64.
///
/// Пример:
/// magnitude(&[0.0, 1.0, 0.0]) = 1.0
fn magnitude(v: &[f64]) -> f64 {
    // Сумма квадратов координат
    let mut sum_sq: f64 = 0.0;

    // Проходим по всем координатам (x, y, z, ...)
    for &coord in v {
        // coord * coord = квадрат координаты
        sum_sq += coord * coord;
    }

    // Корень из суммы квадратов — длина вектора
    // sqrt() есть у f64
    sum_sq.sqrt()
}

/// Нормализует вектор "на месте" (in-place).
///
/// "Нормализовать" значит сделать длину вектора равной 1,
/// сохранив направление.
///
/// Если длина = 0 (нулевой вектор), нормализовать нельзя
/// (деление на 0). В таком случае — паникуем, чтобы ошибка была явной.
fn normalize(v: &mut [f64]) {
    // Сначала узнаём длину
    let mag = magnitude(v);

    // Защита от деления на ноль
    if mag == 0.0 {
        panic!("Нельзя нормализовать нулевой вектор (длина = 0)");
    }

    // Делим каждую координату на длину
    for coord in v.iter_mut() {
        *coord /= mag;
    }
}

// ------------------------------------------------------
// Тесты (cargo test)
// ------------------------------------------------------
#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_magnitude_unit_vector() {
        // Единичный вектор вдоль Y имеет длину 1
        let v = [0.0, 1.0, 0.0];
        assert!((magnitude(&v) - 1.0).abs() < 1e-12);
    }

    #[test]
    fn test_normalize_makes_length_one() {
        let mut v = [1.0, 2.0, 9.0];
        normalize(&mut v);
        // После нормализации длина должна стать 1 (с погрешностью)
        assert!((magnitude(&v) - 1.0).abs() < 1e-12);
    }
}

// ------------------------------------------------------
// main из шаблона — оставляем, он показывает, что всё работает
// ------------------------------------------------------
fn main() {
    println!("Модуль единичного вектора: {}", magnitude(&[0.0, 1.0, 0.0]));

    let mut v = [1.0, 2.0, 9.0];
    println!("Модуль {v:?}: {}", magnitude(&v));
    normalize(&mut v);
    println!("Модуль {v:?} после нормализации: {}", magnitude(&v));
}