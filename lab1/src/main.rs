// Задание 1: Числа Фибоначчи
//
// Fibonacci(0) = 0
// Fibonacci(1) = 1
// Fibonacci(n) = Fibonacci(n-1) + Fibonacci(n-2)
//
// ВАЖНО: в задании просят рекурсивную реализацию.
// Рекурсия = функция вызывает саму себя.
// 
// Типы:
// - n: u32  (беззнаковое 32-битное число, 0..=4294967295)
// - возвращаем u32

// Функция fib вычисляет n-е число Фибоначчи.
fn fib(n: u32) -> u32 {
    // База рекурсии (base case):
    // Если не сделать базу, рекурсия уйдёт в бесконечность и программа упадёт.
    //
    // По определению:
    // fib(0) = 0
    // fib(1) = 1
    if n < 2 {
        // Здесь мы просто возвращаем n:
        // - если n = 0 -> вернётся 0
        // - если n = 1 -> вернётся 1
        return n;
    }

    // Рекурсивный шаг:
    // fib(n) = fib(n-1) + fib(n-2)
    //
    return fib(n - 1) + fib(n - 2);
}

fn main() {
    // В задании n = 20
    let n: u32 = 20;

    // Печатаем результат:
    // {} заменяется значениями аргументов.
    // Здесь будет: fib(20) = 6765
    println!("fib({}) = {}", n, fib(n));
}